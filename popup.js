function formatCnaeCode(raw) {
  const d = String(raw).replace(/\D/g, '');
  // IBGE returns 7 digits: e.g. 4781400 → 47.81-4-00
  if (d.length === 7) return `${d.slice(0,2)}.${d.slice(2,4)}-${d.slice(4,5)}-${d.slice(5,7)}`;
  return raw; // already formatted
}

